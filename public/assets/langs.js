const byCode = new Map(LANGS.map((l) => [l.code.toLowerCase(), l]));
for (const [alias, code] of Object.entries(CODE_ALIASES))
  byCode.set(alias, byCode.get(code.toLowerCase()));
for (const l of LANGS) {
  const base = l.code.split("-")[0].toLowerCase();
  if (!byCode.has(base)) byCode.set(base, l);
}

export const langByCode = (code) => {
  if (!code) return null;
  const c = String(code).toLowerCase();
  return byCode.get(c) || byCode.get(c.split("-")[0]) || null;
};

export const langName = (code) =>
  code === "auto" ? "detect language" : langByCode(code)?.name || code || "";

export const isRtl = (code) =>
  RTL.has(
    String(code || "")
      .split("-")[0]
      .toLowerCase(),
  );

export const flagUrl = (code) => {
  const l = langByCode(code);
  return l ? `/s/flags/${l.flag}.svg` : null;
};

