(() => {
	const solveCaptcha = async (a) => {
		const { solveCaptcha } = await import("/s/captcha.js");
		return solveCaptcha(a);
	};

	const safeUrl = (url) => {
		if (!url) return "#";
		try {
			const parsed = new URL(url);
			if (parsed.protocol === "http:" || parsed.protocol === "https:")
				return url;
		} catch {}
		return "#";
	};

	let allImages = [];
	let selectedIndex = -1;
	let detailPanel = null;
	let isLoading = false;
	let hasMoreResults = true;
	let pk = "__results_pk__";
	const currentQuery = new URLSearchParams(window.location.search).get("q");

	const AI_KEYWORDS = [
		"ai generated",
		"ai-generated",
		"ai art",
		"stable diffusion",
		"generated by",
		"midjourney",
		"ai image generator",
		"generated with ai",
		"images using ai",
		"generated from ai",
		"ai digital art",
	];

	let aiBlocklist = null;
	const fetchBlocklist = async () => {
		try {
			const cachedList = localStorage.getItem("slop:cache");
			const cachedTimestamp = localStorage.getItem("slop:ts");

			if (cachedList && cachedTimestamp) {
				const age = Date.now() - parseInt(cachedTimestamp, 10);
				if (age < 24 * 60 * 60 * 1000) {
					return cachedList;
				}
			}

			const text = await (
				await fetch(
					"https://cdn.jsdelivr.net/gh/laylavish/uBlockOrigin-HUGE-AI-Blocklist@main/list_uBlacklist.txt",
				)
			).text();

			localStorage.setItem("slop:cache", text);
			localStorage.setItem("slop:ts", Date.now().toString());

			return text;
		} catch {
			return localStorage.getItem("slop:cache") || "";
		}
	};

	const parseBlocklist = (text) => {
		const patterns = [];
		const lines = text.split("\n");

		for (const line of lines) {
			const trimmed = line.trim();
			if (!trimmed || trimmed.startsWith("#")) continue;

			if (trimmed.startsWith("/") && trimmed.endsWith("/")) {
				try {
					const regexStr = trimmed.slice(1, -1);
					patterns.push({ type: "regex", pattern: new RegExp(regexStr, "i") });
				} catch (e) {
					console.warn("Invalid regex pattern:", trimmed);
				}
			} else if (trimmed.includes("*")) {
				const regexStr = trimmed
					.replace(/\*:\/\//g, "https?://")
					.replace(/\*/g, ".*")
					.replace(/\./g, "\\.");
				try {
					patterns.push({ type: "glob", pattern: new RegExp(regexStr, "i") });
				} catch (e) {
					console.warn("Invalid glob pattern:", trimmed);
				}
			} else {
				patterns.push({ type: "text", pattern: trimmed.toLowerCase() });
			}
		}

		return patterns;
	};

	const shouldBlockImage = (img) => {
		if (!aiBlocklist) return false;

		const hostname = img.meta_url?.hostname || "";
		const url = img.url || "";

		if (hostname.endsWith(".ai")) {
			return true;
		}

		for (const keyword of AI_KEYWORDS) {
			if (
				(img.title || "")
					.toLowerCase()
					.replaceAll(" ", "")
					.includes(keyword.replaceAll(" ", ""))
			) {
				return true;
			}
		}

		for (const { type, pattern } of aiBlocklist) {
			if (type === "regex" || type === "glob") {
				if (pattern.test(url) || pattern.test(hostname)) {
					return true;
				}
			} else if (type === "text") {
				if (
					url.toLowerCase().includes(pattern) ||
					hostname.toLowerCase().includes(pattern)
				) {
					return true;
				}
			}
		}

		return false;
	};

	const initBlocklist = async () => {
		const hideAiSlop = localStorage.getItem("config:hide_slop") === "true";
		if (!hideAiSlop) {
			aiBlocklist = null;
			return;
		}

		const blocklistText = await fetchBlocklist();
		aiBlocklist = parseBlocklist(blocklistText);
	};

	const createDetailPanel = (img) => {
		const width = img.properties?.width;
		const height = img.properties?.height;

		const panel = document.createElement("div");
		panel.className = "image-detail-panel";

		const header = document.createElement("div");
		header.className = "image-detail-header";

		const prevBtn = document.createElement("button");
		prevBtn.innerHTML = `<svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke="currentColor"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M15 19l-7-7 7-7"/></svg>`;
		prevBtn.title = "Previous";
		prevBtn.onclick = (e) => {
			e.stopPropagation();
			navigateImage(-1);
		};

		const nextBtn = document.createElement("button");
		nextBtn.innerHTML = `<svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke="currentColor"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M9 5l7 7-7 7"/></svg>`;
		nextBtn.title = "Next";
		nextBtn.onclick = (e) => {
			e.stopPropagation();
			navigateImage(1);
		};

		const closeBtn = document.createElement("button");
		closeBtn.innerHTML = `<svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke="currentColor"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M6 18L18 6M6 6l12 12"/></svg>`;
		closeBtn.title = "Close";
		closeBtn.onclick = (e) => {
			e.stopPropagation();
			closeDetailPanel();
		};

		header.append(prevBtn, nextBtn, closeBtn);

		const content = document.createElement("div");
		content.className = "image-detail-content";

		const preview = document.createElement("a");
		preview.className = "image-detail-preview";
		preview.href = safeUrl(img.properties.url);
		preview.target = "_blank";
		preview.rel = "noopener";

		const previewImg = document.createElement("img");
		previewImg.src = `https://external-content.duckduckgo.com/iu/?u=${encodeURIComponent(img.properties.url)}`;
		previewImg.style.aspectRatio = `${width}/${height}`;
		previewImg.alt = img.title;
		preview.append(previewImg);

		const info = document.createElement("div");
		info.className = "image-detail-info";

		const titleEl = document.createElement("h2");
		titleEl.className = "image-detail-title";
		titleEl.textContent = img.title;

		const sourceEl = document.createElement("a");
		sourceEl.className = "image-detail-source";
		sourceEl.href = img.url;
		sourceEl.target = "_blank";
		sourceEl.rel = "noopener";
		sourceEl.textContent = (img.meta_url?.hostname || "").replace(/^www\./, "");

		const dimensionsEl = document.createElement("div");
		dimensionsEl.className = "image-detail-dimensions";
		if (width && height) {
			dimensionsEl.textContent = `${width} × ${height}`;
		}

		const actions = document.createElement("div");
		actions.className = "image-detail-actions";
		const viewLink = document.createElement("a");
		viewLink.href = safeUrl(img.properties.url);
		viewLink.target = "_blank";
		viewLink.rel = "noopener";
		viewLink.textContent = "View file";
		actions.append(viewLink);

		info.append(titleEl, sourceEl, dimensionsEl, actions);
		content.append(preview, info);
		panel.append(header, content);

		return panel;
	};

	const closeDetailPanel = () => {
		if (detailPanel) {
			detailPanel.remove();
			detailPanel = null;
		}
		const selected = document.querySelector(".image-item.selected");
		if (selected) selected.classList.remove("selected");
		selectedIndex = -1;
	};

	const showDetailPanel = (index) => {
		if (index < 0 || index >= allImages.length) return;

		closeDetailPanel();
		selectedIndex = index;

		const img = allImages[index];
		const grid = document.getElementById("images-grid");
		const items = grid.querySelectorAll(".image-item");

		// Find the DOM element that corresponds to this index
		let clickedItem = null;
		for (const item of items) {
			if (parseInt(item.dataset.imageIndex, 10) === index) {
				clickedItem = item;
				break;
			}
		}

		if (!clickedItem) {
			console.warn("Could not find image item for index:", index);
			return;
		}

		clickedItem.classList.add("selected");

		detailPanel = createDetailPanel(img, index);

		const clickedTop = clickedItem.offsetTop;

		let insertAfterItem = clickedItem;
		for (const item of items) {
			const itemIndex = parseInt(item.dataset.imageIndex, 10);
			if (itemIndex > index) {
				const itemTop = item.offsetTop;
				if (Math.abs(itemTop - clickedTop) < 10) {
					insertAfterItem = item;
				} else {
					break;
				}
			}
		}

		insertAfterItem.after(detailPanel);

		requestAnimationFrame(() => {
			const rect = detailPanel.getBoundingClientRect();
			const margin = 50;

			if (rect.top < margin || rect.bottom > window.innerHeight - margin) {
				detailPanel.scrollIntoView({ behavior: "smooth", block: "center" });
			}
		});
	};

	const navigateImage = (direction) => {
		const newIndex = selectedIndex + direction;
		if (newIndex >= 0 && newIndex < allImages.length) {
			showDetailPanel(newIndex);
		}
	};

	const renderImageResult = (img, index) => {
		if (shouldBlockImage(img)) {
			return null;
		}

		const thumbUrl = img.thumbnail || img.properties?.url || "";
		const title = img.title || "";
		const hostname = img.meta_url?.hostname || "";
		const favicon = img.meta_url?.favicon || "";
		const sourceName = hostname.replace(/^www\./, "").split("/")[0];
		const width = img.properties?.width;
		const height = img.properties?.height;

		const link = document.createElement("div");
		link.className = "image-item";
		link.style.cursor = "pointer";
		link.dataset.imageIndex = index;

		const imageWrapper = document.createElement("div");
		imageWrapper.className = "image-wrapper";

		const imgEl = document.createElement("img");
		imgEl.src = thumbUrl;
		imgEl.alt = title;
		imgEl.loading = "lazy";
		imgEl.style.opacity = "0";
		imgEl.onload = () => {
			imgEl.style.opacity = "1";
		};
		imgEl.onerror = () => {
			link.style.display = "none";
		};

		if (width && height) {
			const aspectRatio = width / height;
			const itemWidth = Math.round(180 * aspectRatio);
			link.style.flexBasis = `${itemWidth}px`;
		}

		imageWrapper.append(imgEl);

		const infoDiv = document.createElement("div");
		infoDiv.className = "image-info";

		const titleDiv = document.createElement("div");
		titleDiv.className = "image-title";
		titleDiv.textContent = title;
		infoDiv.append(titleDiv);

		const sourceDiv = document.createElement("div");
		sourceDiv.className = "image-source";

		if (favicon) {
			const faviconImg = document.createElement("img");
			faviconImg.className = "favicon";
			faviconImg.src = favicon;
			faviconImg.alt = "";
			faviconImg.onerror = () => {
				faviconImg.style.display = "none";
			};
			sourceDiv.append(faviconImg);
		}

		if (sourceName) {
			const sourceNameSpan = document.createElement("span");
			sourceNameSpan.className = "source-name";
			sourceNameSpan.textContent = sourceName;
			sourceDiv.append(sourceNameSpan);
		}

		infoDiv.append(sourceDiv);

		link.append(imageWrapper);
		link.append(infoDiv);

		link.addEventListener("click", (e) => {
			e.preventDefault();
			showDetailPanel(index);
		});

		return link;
	};

	const renderImages = (results) => {
		const grid = document.getElementById("images-grid");
		const frag = document.createDocumentFragment();

		if (!results || !results.length) {
			const noResults = document.createElement("div");
			noResults.className = "no-results";
			noResults.textContent = "No images found";
			frag.append(noResults);
		} else {
			allImages = results;
			for (let i = 0; i < results.length; i++) {
				const element = renderImageResult(results[i], i);
				if (element) frag.append(element);
			}
		}

		grid.append(frag);
	};

	const appendImages = (results) => {
		const grid = document.getElementById("images-grid");
		const frag = document.createDocumentFragment();
		const startIndex = allImages.length;

		for (let i = 0; i < results.length; i++) {
			allImages.push(results[i]);
			const element = renderImageResult(results[i], startIndex + i);
			if (element) frag.append(element);
		}

		grid.append(frag);
	};

  const loadMoreImages = async () => {
		if (isLoading || !hasMoreResults || !currentQuery) return;
		isLoading = true;
		const loadingEl = document.getElementById("loading-indicator");
		loadingEl.style.display = "flex";

		try {
			const res = await fetch("/p", {
				method: "POST",
				headers: {
					"X-Galileo-Hash": [...`${currentQuery}${pk}`]
						.reduce((h, c) => (h * 31 + c.charCodeAt(0)) | 0, 0)
						.toString(16),
					"X-Galileo-JWT": "__results_cl__",
					"X-Galileo-Pass": "%%galileo_pass%%",
				},
				body: pk,
			});

			pk = res.headers.get("x-galileo-upk") || pk;
			const newData = await res.json();

			if (newData.captchaHtml) {
				await solveCaptcha(newData.captchaHtml);
				loadMoreImages();
				return;
			}

			if (newData.error || !newData.results?.length) {
				hasMoreResults = false;
			  loadingEl.style.display = "none";
				if (!newData.results?.length) {
					const endEl = document.createElement("div");
					endEl.className = "end-of-results";
					endEl.textContent = "No more images";
					document.getElementById("images-grid").append(endEl);
				}
				return;
			}

      appendImages(newData.results);
			hasMoreResults =
				newData.more_results_available !== false && newData.results.length > 0;
		} catch (err) {
			console.error("failed to load more images:", err);
		} finally {
			isLoading = false;
			if (document.getElementById("loading-indicator")) {
				document.getElementById("loading-indicator").style.display = "none";
			}
		}
	};

	document.addEventListener("keydown", (e) => {
		if (selectedIndex === -1) return;

		if (e.key === "Escape") {
			closeDetailPanel();
		} else if (e.key === "ArrowLeft") {
			navigateImage(-1);
		} else if (e.key === "ArrowRight") {
			navigateImage(1);
		}
	});

	const optionsBtn = document.querySelector("#options-btn");
	const optionsPopup = document.querySelector("#options-popup");
	const hideAiSlopCheckbox = document.querySelector("#hide-ai-slop");

	const savedSetting = localStorage.getItem("config:hide_slop") === "true";
	hideAiSlopCheckbox.checked = savedSetting;

	optionsBtn?.addEventListener("click", (e) => {
		e.stopPropagation();
		const isVisible = optionsPopup.classList.contains("visible");
		optionsPopup.classList.toggle("visible");

		if (!isVisible) {
			const rect = optionsBtn.getBoundingClientRect();
			optionsPopup.style.position = "absolute";
			optionsPopup.style.left = `${rect.left - 220}px`;
		}
	});

	document.addEventListener("click", (e) => {
		if (!optionsPopup.contains(e.target) && e.target !== optionsBtn) {
			optionsPopup.classList.remove("visible");
		}
	});

	hideAiSlopCheckbox?.addEventListener("change", async (e) => {
		const isEnabled = e.target.checked;
		localStorage.setItem("config:hide_slop", isEnabled.toString());

		await initBlocklist();

		const grid = document.getElementById("images-grid");
		grid.innerHTML = "";
		renderImages(allImages);
	});

	(async () => {
		await initBlocklist();

    const data = __results_template__;
    hasMoreResults = data.more_results_available !== false;

		if (data.captchaHtml) {
			solveCaptcha(data.captchaHtml).then(() => {
				location.reload();
			});
			return;
    }
		
		renderImages(data.results);

		const observer = new IntersectionObserver(
      (entries) => {
        if (entries[0].isIntersecting) {
					loadMoreImages();
				}
			},
			{
				rootMargin: "400px",
			},
		);

		observer.observe(document.querySelector("#load-more-sentinel"));
	})();
})();
